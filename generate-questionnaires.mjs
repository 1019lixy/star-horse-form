// ── Layout helpers ──
function box2(a, b) {
  const id = mkId(); const name = fid();
  return {
    id, compType: 'container', itemType: 'box', fieldName: name, rowNums: 1, columnNums: 2,
    preps: {
      id, label: 'Grid Layout', name, itemNameLabel: 'Grid Layout',
      elements: [{
        id: 'row-' + uid(), rowIndex: 1,
        columns: [
          { id: 'col-' + uid(), cellVisible: true, colIndex: 1, colspan: 12, items: [a] },
          { id: 'col-' + uid(), cellVisible: true, colIndex: 1, colspan: 12, items: [b] },
        ],
      }],
    },
  };
}

function card(label, items) {
  const flatItems = items.flat(Infinity);
  const id = mkId(); const name = fid();
  return {
    id, compType: 'container', itemType: 'card', fieldName: name,
    preps: {
      id, label, name, itemNameLabel: label,
      headerFieldList: [],
      elements: [{ label, tabName: label, objectName: label, subFormFlag: 'Y', items: flatItems }],
    },
  };
}

function save(filename, list) {
  const data = {
    formInfo: { formName: filename.replace('.json', ''), currentPageType: 'pc' },
    dataList: list,
    exportTime: new Date().toISOString(),
    version: '1.0',
  };
  writeFileSync(join(OUT, filename), JSON.stringify(data, null, 2), 'utf8');
  console.log(`  ✓ ${filename}`);
}

