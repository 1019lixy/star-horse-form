import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';
import { randomUUID } from 'crypto';

const OUT = 'template';
mkdirSync(OUT, { recursive: true });

const uid = () => randomUUID();
const fid = () => 'field_' + uid().substring(0, 6);
const mkId = () => 'Id' + uid();

function mkField(itemType, label, opts = {}) {
  const id = mkId();
  const name = fid();
  return {
    id, compType: 'formItem', itemType, fieldName: name,
    preps: {
      id, label, name, itemNameLabel: label,
      formVisible: opts.formVisible !== false,
      listVisible: opts.listVisible !== false,
      viewVisible: opts.viewVisible !== false,
      searchVisible: false,
      required: opts.required || false,
      disabled: false,
      clearable: true,
      readonly: false,
      maxLength: opts.maxLength || 255,
      placeholder: opts.placeholder || `请输入${label}`,
      dataSource: opts.dataSource || '',
      values: opts.values || [],
      rules: [],
      helpMsg: opts.helpMsg || '',
      ...opts.extra,
    },
  };
}

// ── Survey-specific helpers ──

/** Radio single-choice question: produces radio type with {name, value} options */
function radio(label, options, opts = {}) {
  return mkField('radio', label, {
    ...opts,
    dataSource: 'data',
    values: options.map((v, i) => ({ name: v, value: String(i + 1) })),
  });
}

/** Checkbox multi-select question */
function checkbox(label, options, opts = {}) {
  return mkField('checkbox', label, {
    ...opts,
    dataSource: 'data',
    values: options.map((v, i) => ({ name: v, value: String(i + 1) })),
  });
}

/** Radio with "其他" option + hidden text input that appears when "其他" is selected */
function radioWithOther(label, options, opts = {}) {
  const r = radio(label, [...options, '其他'], opts);
  const inp = mkField('input', `${label}(其他请说明)`, { formVisible: false, placeholder: '请填写具体说明' });
  return [r, inp];
}

/** Checkbox with "其他" option + hidden text input */
function checkboxWithOther(label, options, opts = {}) {
  const cb = checkbox(label, [...options, '其他'], opts);
  const inp = mkField('input', `${label}(其他请说明)`, { formVisible: false, placeholder: '请填写具体说明' });
  return [cb, inp];
}

/** Rate/star rating question */
function rate(label) { return mkField('rate', label); }

/** Textarea open-ended question (used sparingly) */
function textarea(label) { return mkField('textarea', label, { maxLength: 1000, placeholder: '请详细描述您的想法' }); }

